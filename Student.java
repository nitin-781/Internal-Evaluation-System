public class Student {
    public int no;
    public String id,name;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Student(int no, String id, String name) {
        this.no = no;
        this.id = id;
        this.name = name;
    }

    public Student() {
    }
    
}
